@page modules Modules

A Xataface Module is a self-contained package that defines functionality that can be included in multiple Xataface applications.  Modules may provide features such as:

-# Widgets
-# Templates
-# Themes
-# Actions
-# Blocks & Slots
-# Triggers
-# More...

@section module_vs_app What is the Difference Between a Module and An Application?

A module provides additional features to Xataface and can be used across multiple applications.  An application, on the other hand is built on top of Xataface and cannot be used in any other applications.

